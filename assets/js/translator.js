function updateLanguage(lang) {
    console.log('Updating language to:', lang); // Debug log
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        console.log('Processing element with key:', key); // Debug log
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        } else {
            console.log('Translation not found for key:', key, 'in language:', lang); // Debug log
        }
    });
    localStorage.setItem('language', lang);
}

// Cargar el idioma guardado o usar español por defecto
document.addEventListener('DOMContentLoaded', () => {
    const savedLanguage = localStorage.getItem('language') || 'es';
    console.log('Loading saved language:', savedLanguage); // Debug log
    const languageSelector = document.getElementById('language-selector');
    if (languageSelector) {
        languageSelector.value = savedLanguage;
        updateLanguage(savedLanguage);
    } else {
        console.error('Language selector not found!'); // Debug log
    }
}); 